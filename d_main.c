//
// FixedDiv, C version.
//

fixed_t32
FixedDiv
(fixed_t32	a,
	fixed_t32	b)
{
	if ((abs(a) >> 14) >= abs(b))
		return (a^b) < 0 ? MINLONG : MAXLONG;
	return FixedDiv2(a, b);
}



fixed_t32
FixedDiv2
(fixed_t32	a,
	fixed_t32	b)
{
 

	double c;

	c = ((double)a) / ((double)b) * FRACUNIT;

	if (c >= 2147483648.0 || c < -2147483648.0)
		I_Error("FixedDiv: divide by zero");
	return (fixed_t32)c;
}
