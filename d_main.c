//
// IdentifyVersion
// Checks availability of IWAD files by name,
// to determine whether registered/commercial features
// should be executed (notably loading PWAD's).
//
void IdentifyVersion (void)
{
    strcpy(basedefault,"default.cfg");
	 

 

    if ( !access ("doom2.wad",R_OK) )
    {
        commercial = true;
        D_AddFile ("doom2.wad");
        return;
    }

#if (EXE_VERSION >= EXE_VERSION_FINAL)
    if ( !access ("plutonia.wad", R_OK ) )
    {
      commercial = true;
      plutonia = true;
      D_AddFile ("plutonia.wad");
      return;
    }

    if ( !access ( "tnt.wad", R_OK ) )
    {
      commercial = true;
      tnt = true;
      D_AddFile ("tnt.wad");
      return;
    }
#endif

    if ( !access ("doom.wad",R_OK) )
    {
      registered = true;
      D_AddFile ("doom.wad");
      return;
    }

    if ( !access ("doom1.wad",R_OK) )
    {
      shareware = true;
      D_AddFile ("doom1.wad");
      return;
    }

    printf("Game mode indeterminate.\n");
    exit(1);
    //I_Error ("Game mode indeterminate\n");
}

//
// Find a Response File
//
void FindResponseFile (void)
{
	int16_t             i;
#define MAXARGVS        100
        
    for (i = 1;i < myargc;i++)
        if (myargv[i][0] == '@')
        {
            FILE *          handle;
			int32_t             size;
			int16_t             k;
			int16_t             index;
			int16_t             indexinfile;
			int8_t    *infile;
			int8_t    *file;
			int8_t    *moreargs[20];
			int8_t    *firstargv;
                        
            // READ THE RESPONSE FILE INTO MEMORY
            handle = fopen (&myargv[i][1],"rb");
            if (!handle)
            {
                printf ("\nNo such response file!");
                exit(1);
            }
            printf("Found response file %s!\n",&myargv[i][1]);
            fseek (handle,0,SEEK_END);
            size = ftell(handle);
            fseek (handle,0,SEEK_SET);
            file = malloc (size);
            fread (file,size,1,handle);
            fclose (handle);
                        
            // KEEP ALL CMDLINE ARGS FOLLOWING @RESPONSEFILE ARG
            for (index = 0,k = i+1; k < myargc; k++)
                moreargs[index++] = myargv[k];
                        
            firstargv = myargv[0];
            myargv = malloc(sizeof(int8_t *)*MAXARGVS);
            memset(myargv,0,sizeof(int8_t *)*MAXARGVS);
            myargv[0] = firstargv;
                        
            infile = file;
            indexinfile = k = 0;
            indexinfile++;  // SKIP PAST ARGV[0] (KEEP IT)
            do
            {
                myargv[indexinfile++] = infile+k;
                while(k < size &&
                      ((*(infile+k)>= ' '+1) && (*(infile+k)<='z')))
                    k++;
                *(infile+k) = 0;
                while(k < size &&
                      ((*(infile+k)<= ' ') || (*(infile+k)>'z')))
                    k++;
            } while(k < size);
                        
            for (k = 0;k < index;k++)
                myargv[indexinfile++] = moreargs[k];
            myargc = indexinfile;
        
            // DISPLAY ARGS
            printf("%d command-line args:\n",myargc);
            for (k=1;k<myargc;k++)
                printf("%s\n",myargv[k]);

            break;
        }
}


